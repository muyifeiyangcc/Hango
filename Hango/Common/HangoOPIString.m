#import "HangoOPIString.h"

static NSString *HangoDecodeOPIBytes(const uint8_t *bytes, NSUInteger length) {
    if (length == 0) {
        return @"";
    }
    NSMutableData *data = [NSMutableData dataWithLength:length];
    uint8_t *out = data.mutableBytes;
    const uint8_t key = 0x5A;
    for (NSUInteger i = 0; i < length; i++) {
        out[i] = bytes[i] ^ key;
    }
    return [[NSString alloc] initWithBytes:out length:length encoding:NSUTF8StringEncoding] ?: @"";
}

#define HANGO_DEFINE_OPI_STRING(fn, ...) \
NSString *fn(void) { \
    static NSString *value; \
    static dispatch_once_t onceToken; \
    dispatch_once(&onceToken, ^{ \
        static const uint8_t bytes[] = { __VA_ARGS__ }; \
        value = HangoDecodeOPIBytes(bytes, sizeof(bytes)); \
    }); \
    return value; \
}

